//find area of rectangle
#include<stdio.h>
int main()
{
   int area,length,width;
   
   printf("Length of rectangle:-");
   scanf("%d",&length);
   
   printf("Width of rectangle:-");
   scanf("%d",&width);
   
   area=length*width;
   
   printf("Area of rectangle is:-%d",area);
   
    return 0;
}
